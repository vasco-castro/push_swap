#include "operations.h"

static void	swap(t_stacks *stacks)
{
	void (stacks);
}

void	sa(t_stacks *stacks)
{
	swap(a);
	write(1, "sa\n", 3);
}

void	sb(t_stacks *stacks)
{
	swap(b);
	write(1, "sb\n", 3);
}

